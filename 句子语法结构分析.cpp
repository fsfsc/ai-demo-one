/*五种基本短语结构类型是什么？
主谓短语、动宾短语、偏正短语、中补短语、联合短语。

1、主谓短语：

主谓短语是短语的结构分类之一，它由两个成分组成。前一个成分的功能是提出主题，称为主语；后一个成分对主题加以陈述，称为谓语。主语和谓语构成了主谓关系（或主述关系，即“主题―陈述”关系）。

2、动宾短语：

动宾短语是由的两个成分组成都是支配涉及关系，动语也就是在前面起支配作用的部分，因此表示的是动作行为；而在宾语则是后面被支配的部分，表示的意思是做什么、是什么。

3、偏正短语：

偏正短语又叫偏正词组，是由修饰语和中心语组成，结构成分之间有修饰与被修饰关系的短语；动词、名词、形容词与它们前面起修饰作用的成份组成的短语。名词前的修饰成份是定语；定语（状语）和中心语的关系是偏和正的关系；偏正短语包括定中短语与状中短语。

4、中补短语：

中补短语也叫后补短语，由动词或形容词与后面起补充作用的成分组合而成，常用“得”字表示，起补充作用的成分是补语。还可分为两种：动补短语：动+补。

5、联合短语：

由语法地位平等的两项或几项组成，其间是联合关系，可细分为并列、递进、选择等关系。有时用“和”、“并”、“或”等连词表示。例如：今天和明天（名+名，并列）。
*/

